    function _updateUserLocationMarker(latlng) {
        if (!assets.hasOwnProperty('userLocationMarker')) {
            assets.userLocationMarker = new google.maps.Marker({
                'map': controls.map,
                'position': latlng
            });
        } else {
            assets.userLocationMarker.setPosition(latlng);
        }
    }

    function _zoomToBranchArea(branchName) {
        layers.branches.forEach(function(branchFeature) {
            if (branchName == branchFeature.getProperty('name')) {
                let bounds = new google.maps.LatLngBounds();
                branchFeature.getGeometry().forEachLatLng(function(latlng) {
                    bounds.extend(latlng);
                });
                controls.map.fitBounds(bounds);
            }
        });
    }

    function _defaultAllBranches() {
        layers.branches.forEach(function(branchFeature) {
            layers.branches.overrideStyle(branchFeature, {
                'zIndex': 0
            });
        });
        layers.branches.revertStyle();
    }

    function _highlightBranch(branchName) {
        layers.branches.forEach(function(branchFeature) {
            if (branchName == branchFeature.getProperty('name')) {
                layers.branches.overrideStyle(branchFeature, {
                    'strokeColor': '#FFFFFF',
                    'zIndex': 9999,
                    'strokeWeight': 2,
                    'fillOpacity': 0.5
                });
            }
        });
    }

    function _branchesLayerInit() {
        layers.branches = new google.maps.Data({
            'map': controls.map
        });

        layers.branches.addGeoJson(branches_geojson, {
            'idPropertyName': 'name'
        });

        layers.branches.setStyle({
            'fillColor': '#FF0000',
            'fillOpacity': 0.3,
            'strokeColor': '#666',
            'strokeWeight': 1
        });

        layers.branches.addListener('mouseover', function(event) {
            _defaultAllBranches();
            _highlightBranch(event.feature.getProperty('name'));
        });

        layers.branches.addListener('mouseout', function(event) {
            _defaultAllBranches();
        });

        layers.branches.addListener('click', function(event) {
            _zoomToBranchArea(event.feature.getProperty('name'));
            var featureData = Array();
            featureData[0] = event.feature.getProperty('name');
            featureData[1] = event.feature.getProperty('branch_phone');
            featureData[2] = event.feature.getProperty('branch_email');
            featureData[3] = event.feature.getProperty('branch_slug');

            controlsDom.selectRegion.value = featureData[0];
            controlsDom.addressInput.value = '';

            _displayInformationBox(featureData);
        });
    }

